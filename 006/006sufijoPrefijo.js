/* 006sufijoPrefijo.js/.html: ¿Cuáles son los valores finales de todas las variables
a, b, c y d después del código a continuación?*/

let a = 1, b = 1;
let c = ++a; // aquí sumamos +1 al valor de a antes de asignarselo a c
let d = b++; // en este caso sumamos +1 a b después de asignarselo a d

// a=2
// b=2
// c=2
// d=1